static const CodePage* FindCodePage(const char* pszName)
{
    int i = 0;
    while (CodePageArray[i]._pszName) {
        if (CRegExp::Match(std::string(pszName), std::string("^(?i)") + CodePageArray[i]._pszName + "$"))
            break;
        i++;
    }
    return (CodePageArray[i]._pszName) ? &CodePageArray[i] : 0;
}

bool CharacterMapper::IsSupported(const std::string& sEnc)
{
    return FindCodePage(sEnc.c_str()) != 0 || IsEqual(sEnc, "utf-8") || IsEqual(sEnc, "utf8");
}

inline int CharacterMapper::Map(int c) const
{
    if (_fromUtf8)
        return (int)'?';
    if (!_bInitialised)
        return c;
    else if (_pcCharMap)
        return _pcCharMap[c & 0xffff];
    else
        return _pwWordMap[c & 0xff];
}

std::string CharacterMapper::Map(const std::string& sText) const
{
    if (_fromUtf8) {
        std::string utf8 = sText;
        return Utf8toIso885915(utf8);
    }
    if (_toUtf8) {
        std::string iso = sText;
        return iso885915ToUtf8(iso);
    }
    std::string enc;
    std::string::const_iterator i;
    if (!_bInitialised)
        return sText;
    for (i = sText.begin(); i != sText.end(); i++) {
        enc += char(Map(int(*i)));
    }
    return enc;
}

CharacterMapper::CharacterMapper(const char* pszFrom, const char* pszTo)
{
    Init(pszFrom, pszTo);
}

CharacterMapper::~CharacterMapper()
{
    delete[] _pcCharMap;
    delete[] _pwWordMap;
}

void CharacterMapper::Init(const char* pszFrom, const char* pszTo)
{
    const _CodePage* poCP;
    int i;

    if ((IsEqual(pszFrom, "utf-8") || IsEqual(pszFrom, "utf8")) && IsEqual(pszTo, "iso-8859-1")) {
        _fromUtf8 = true;
        _bInitialised = true;
        return;
    }
    if (IsEqual(pszFrom, "iso-8859-1") && (IsEqual(pszTo, "utf-8") || IsEqual(pszTo, "utf8"))) {
        _toUtf8 = true;
        _bInitialised = true;
        return;
    }
    delete[] _pcCharMap;
    _pcCharMap = 0;
    delete[] _pwWordMap;
    _pwWordMap = 0;

    if (IsEqual(pszFrom, pszTo))
        return;

    if (!IsEqual(pszFrom, "UNICODE")) {
        // Ausgangszeichensatz ist 8-Bit
        poCP = FindCodePage(pszFrom);
        if (!poCP) {
            ERRMSG(0, ("Fehler: Keine Zeichenkonvertierung fuer '%s' bekannt.", pszFrom));
            return;
        }
        _pwWordMap = new UCS2[256];
        for (i = 0; i < 256; i++)
            _pwWordMap[i] = poCP->_pwMap[i];
        if (!IsEqual(pszTo, "UNICODE")) {
            // Zielzeichensatz ist 8-Bit
            CharacterMapper oCM("UNICODE", pszTo);
            if (!oCM.IsOK())
                return;
            for (i = 0; i < 256; i++)
                _pwWordMap[i] = uint16_t(oCM.Map(_pwWordMap[i]));
        }
    }
    else {
        // Ausganszeichensatz ist Unicode
        poCP = FindCodePage(pszTo);
        if (!poCP) {
            ERRMSG(0, ("Fehler: Keine Zeichenkonvertierung fuer '%s' bekannt.", pszTo));
            return;
        }
        _pcCharMap = new char[65536];
        memset(_pcCharMap, (int)'?', 65536);
        for (i = 0; i < 256; i++)
            _pcCharMap[poCP->_pwMap[i]] = char(i);
    }
    _bInitialised = true;
}
